    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        this.setAlwaysOnTop(false);
        try {
        ResultSet rs;
        int cod_act = -1, tipact = cmbtipact.getSelectedIndex() + 1 ;
        rs = H.Buscar("SELECT cod_act FROM Actividades WHERE nom_act = '"+H.nom_act+"'");
        
            while(rs.next()){
                    cod_act = rs.getInt(1);                
            }
         
        H.Actualizar("UPDATE Actividades SET nom_act = '"+H.encriptar(txtNomAct.getText())+"', des_act = '"+H.encriptar(txtDescripcion.getText())+"', fecha_act = '"+cmbDia.getSelectedItem().toString() +"/"+cmbMes.getSelectedItem().toString()+"/"+cmbA単o.getSelectedItem().toString()+"', hora_act = '"+cmbHoras.getSelectedItem().toString() +":"+cmbMinutos.getSelectedItem().toString()+"', lugar_act = '"+H.encriptar(txtLugar.getText())+"', id_tipoact = "+tipact+" WHERE cod_act = "+cod_act+"");
        frmActividades.sql = 1;
        this.dispose();
    }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

