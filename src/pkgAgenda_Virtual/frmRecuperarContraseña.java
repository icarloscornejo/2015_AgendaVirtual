    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        try{
        this.setAlwaysOnTop(false);
        if(!"".equals(txtUsuario.getText()) && !"".equals(txtCorreo.getText())){
            rs = H.Buscar("SELECT correo_usu, contra_usu FROM Usuarios WHERE usu_usu = '"+H.encriptar(txtUsuario.getText())+"'");
            rs.next();
            correo = H.desencriptar(rs.getString(1));
            contra = H.desencriptar(rs.getString(2));
            if(!"".equals(correo) && !"".equals(contra)){
            if(enviarEmail() == true){
                JOptionPane.showMessageDialog(null, "Mensaje enviado con exito.");
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Mensaje no enviado.");
            }
            }
        }else{
            JOptionPane.showMessageDialog(null, "No deje campos vacios.");
        }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

