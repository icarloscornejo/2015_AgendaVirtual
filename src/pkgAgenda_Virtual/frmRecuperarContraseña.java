    private boolean enviarEmail() {
    try
    {
        
        // Propiedades de la conexión
        Properties props = new Properties();
        props.setProperty("mail.smtp.host",  "smtp.gmail.com");
        props.setProperty("mail.smtp.starttls.enable", "true");
        props.setProperty("mail.smtp.port", "587");
        props.setProperty("mail.smtp.auth", "true");
 
        // Preparamos la sesion
        Session session = Session.getDefaultInstance(props);
        
    //Recoger los datos
    String str_De       = "agendavirtual2.0@gmail.com";
    String str_PwRemitente      = H.contra;            
    String str_Para     = correo;
    String str_Asunto = "Recuperacion de contraseña.";
    String str_Mensaje = "Su contraseña de usuario es: '"+contra+"'.";
                 
        // Construimos el mensaje
        MimeMessage message = new MimeMessage(session);
         
        message.setFrom(new InternetAddress( str_De ));    
         
        //receptores.
        message.addRecipients(Message.RecipientType.TO, str_Para);        
        message.setSubject( str_Asunto );        
        message.setText( str_Mensaje );
             
        // Lo enviamos.
        Transport t = session.getTransport("smtp");
        t.connect(str_De, str_PwRemitente);
        t.sendMessage(message, message.getRecipients(Message.RecipientType.TO));
                 
        // Cierre de la conexion.
        t.close();
        return true;
    }
    catch (MessagingException e)
    {
        JOptionPane.showMessageDialog(null, e.toString());
        return false;
    }       
    }
    
